"""

Este módulo fornece funções de operações matemáticas básicas, como por exemplo:
somar, subtrair, dividir e multiplicar. Assim como também imprime variáveis.

"""

def somar(a, b):
    print(a + b)

def subtrair(a, b):
    print(a - b)

def dividir(a, b):
    print(a / b)
    
def multiplicar(a, b):
    print(a * b)

def imprimir(val):
    print(val)